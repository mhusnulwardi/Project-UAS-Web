    <h2 align="center">Kain Songket</h2>
      <article>
        <p align="justify">
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kain songket merupakan kain tenun khas Indonesia yang sudah terkenal keindahannya hingga ke mancanegara. Warnanya yang indah serta <i><u>motifnya</u></i> yang cantik membuat banyak orang tertarik untuk memilikinya.  Beberapa orang mengatakan, kata songket berasal dari istilah kata sungkit dalam bahasa Melayu. Kata sungkit berarti “mencukil” atau “mengait”. Hal ini dipengaruhi oleh metode pembuatannya yaitu mengaitkan kain tenun dan memasukkan benang emas.

          Namun ternyata ada teori lain mengenai asal kata songket, yaitu berasal dari kata di-songsong dan di-teket. Di-songsong karena dalam pembuatannya menggunakan alat yang dilempar diantara benang lalu disongsong atau dijemput. Sedangkan teket dalam bahasa Palembang lama memiliki arti sulam. Pembuatan kain songket dapat memakan waktu yang cukup lama. Untuk membuat satu lembar kain songket hingga selesai dibutuhkan waktu hingga 3 bulan. 
        </p>
